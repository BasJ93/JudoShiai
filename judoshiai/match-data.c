gint is_special_match(struct compsys sys, gint match, gint *intval, double *doubleval, double *doubleval2)
{
    *intval = 0;
    *doubleval = *doubleval2 = 0.0;

    switch (sys.table) {
    case TABLE_DOUBLE_REPECHAGE:
        switch (sys.system) {
        case  SYSTEM_FRENCH_128:
            switch (match) {
            case 141:
                *intval = 1;
                *doubleval = 0.50;
                *doubleval2 = 0.54;
                return SPECIAL_MATCH_X_Y;
            case 142:
                *intval = 1;
                *doubleval = 0.58;
                *doubleval2 = 0.62;
                return SPECIAL_MATCH_X_Y;
            }
        }
        break;
    case TABLE_SWE_DUBBELT_AATERKVAL:
    case TABLE_SWE_DIREKT_AATERKVAL:
    case TABLE_EST_D_KLASS:
    case TABLE_NO_REPECHAGE:
    case TABLE_SWE_ENKELT_AATERKVAL:
        break;
    case TABLE_ESP_DOBLE_PERDIDA:
        switch (sys.system) {
        case  SYSTEM_FRENCH_8:
            switch (match) {
            case 7:
            case 8:
                return SPECIAL_MATCH_STOP;
            case 9:
            case 10:
                *intval = F_REPECHAGE;
                return SPECIAL_MATCH_STOP;
            case 5:
            case 6:
                return SPECIAL_MATCH_START;
            case 11:
            case 12:
                return SPECIAL_MATCH_START;
            case 13:
                *intval = F_REPECHAGE;
                return SPECIAL_MATCH_FLAG;
            }
        case  SYSTEM_FRENCH_16:
            switch (match) {
            case 21:
            case 22:
                return SPECIAL_MATCH_STOP;
            case 25:
            case 26:
                *intval = F_REPECHAGE;
                return SPECIAL_MATCH_STOP;
            case 9:
            case 10:
            case 11:
            case 12:
                return SPECIAL_MATCH_START;
            case 27:
            case 28:
                return SPECIAL_MATCH_START;
            case 29:
                *intval = F_REPECHAGE;
                return SPECIAL_MATCH_FLAG;
            }
        }
        break;
        /*case TABLE_ESP_REPESCA_DOBLE_INICIO:*/
    case TABLE_ESP_REPESCA_DOBLE: // IJF
        switch (sys.system) {
        case  SYSTEM_FRENCH_128:
            switch (match) {
            case 127:
                *intval = 1;
                *doubleval = 0.50;
                *doubleval2 = 0.54;
                return SPECIAL_MATCH_X_Y;
            case 128:
                *intval = 1;
                *doubleval = 0.58;
                *doubleval2 = 0.62;
                return SPECIAL_MATCH_X_Y;
            }
            break;
        }
        break;
    case TABLE_ESP_REPESCA_SIMPLE:
        break;
    case TABLE_MODIFIED_DOUBLE_ELIMINATION:
        switch (sys.system) {
        case  SYSTEM_FRENCH_64:
            switch (match) {
            case 117:
            case 118:
                *intval = F_REPECHAGE;
                return SPECIAL_MATCH_STOP;
            case 121:
                *intval = 0;
                *doubleval = 0.80;
                *doubleval2 = 0.84;
                return SPECIAL_MATCH_X_Y;
            case 122:
                *intval = 0;
                *doubleval = 0.88;
                *doubleval2 = 0.92;
                return SPECIAL_MATCH_X_Y;
            case 124:
                *intval = 3;
                *doubleval = 0.80;
                *doubleval2 = 0.84;
                return SPECIAL_MATCH_X_Y;
            }
        }
        break;
    }

    return 0;
}

